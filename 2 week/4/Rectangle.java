public class Rectangle {
    protected int xLeft;
    protected int yLeft;
    protected int xRight;
    protected int yRight;

    public Rectangle(int xLeft, int yLeft, int xRight, int yRight) {
        this.xLeft = xLeft;
        this.yLeft = yLeft;
        this.xRight = xRight;
        this.yRight = yRight;
    }
}